	struct Tree {
		size_t id; // initial id
		size_t parent; // direct parent
		size_t desc_cnt; // all descendants count
		vector<size_t> children; // direct children
		size_t next = -1; // next vertex to needed

		void path_dfs(size_t nxt) {
			next = nxt;
			if (id == 0)
				return;
			trees[parent].path_dfs(id);
		}

		size_t descendants_dfs() {
			desc_cnt = 0;
			for (size_t child : children)
				desc_cnt += trees[child].descendants_dfs();
			return desc_cnt + 1;
		}

		size_t down_dfs(size_t num) {
			if (next == -1)
				return num += 1 + desc_cnt;
			if (id == needed) {
				possible_ids.emplace(num);
				return -1;
			}

		}
	};

